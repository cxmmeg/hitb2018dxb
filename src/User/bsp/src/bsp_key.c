/* 按键口对应的RCC时钟 */
#define RCC_ALL_KEY 	(RCC_APB2Periph_GPIOA | RCC_APB2Periph_GPIOB)

#define KEY_LEFT	PAin(15)
#define KEY_RIGHT	PBin(11)
#define KEY_UP		PBin(10)
#define KEY_DOWN	PBin(12)

#define KEY_OK		PAin(1)
